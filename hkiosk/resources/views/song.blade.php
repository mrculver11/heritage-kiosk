		<h1>THE QUARTERMASTER SONG</h1>
		<p>On the eve of World War II a trio of QM songwriters came up with an official song for the Corps: "We're The Gang That Keeps Things Moving." It was formally approved by The Quartermaster General in October 1941 and steadily rose on the military service Hit Parade in the days after Pearl Harbor. The first verse and chorus went like this:</p>
	</div>
	<div>
		<p>
		When there is trouble brewing at the front,	
		And trucks are rumbling up the road,
		The men who bear the stormy battle's brunt
		Know who is there to share the load;
		They know who'll go through fiery shot and shell
		To bring supplies for men and gun as well;
		You can be sure the Q.M.'s coming out
		For you can hear the soldiers shout:
		CHORUS: We’re the team that keeps things moving,
		At the front and post to post,
		When a Soldier has tough going,
		We’re the team you need the most.
		From the day you join the Army
		‘Til the time you’re home once more,
		If you have to shoot or ride
		The Soldiers fighting by your side,
		Will be from the Quartermaster Corps.
</p>
