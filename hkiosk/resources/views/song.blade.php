		<h1>THE QUARTERMASTER SONG</h1>
		<p>On the eve of World War II a trio of QM songwriters came up with an official song for the Corps: "We're The Gang That Keeps Things Moving." It was formally approved by The Quartermaster General in October 1941 and steadily rose on the military service Hit Parade in the days after Pearl Harbor. The first verse and chorus went like this:</p>
	</div>
	<div class="lyrics">
		<p>
		When there is trouble brewing at the front,	<br>
		And trucks are rumbling up the road, <br>
		The men who bear the stormy battle's brunt <br>
		Know who is there to share the load; <br>
		They know who'll go through fiery shot and shell <br>
		To bring supplies for men and gun as well; <br>
		You can be sure the Q.M.'s coming out <br>
		For you can hear the soldiers shout: <br>
	</div>
	<div class="lyrics2">
		CHORUS: We’re the team that keeps things moving, <br>
		At the front and post to post, <br>
		When a Soldier has tough going, <br>
		We’re the team you need the most. <br>
		From the day you join the Army <br>
		‘Til the time you’re home once more, <br>
		If you have to shoot or ride <br>
		The Soldiers fighting by your side, <br>
		Will be from the Quartermaster Corps. <br>
</p>
